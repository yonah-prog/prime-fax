interface SidebarContentProps {
  role: string
  unreadCount: number
  onClose?: () => void
}

function SidebarContent({ role, unreadCount, onClose }: SidebarContentProps) {
  const pathname = usePathname()
  const [open, setOpen] = useState<Record<string, boolean>>({ Faxing: true, Account: true, Numbers: true, Admin: false })

  const toggle = (title: string) => setOpen((prev) => ({ ...prev, [title]: !prev[title] }))
  const isAdmin = role === "admin"

  return (
    <>
      {/* Logo */}
      <div className="px-4 py-4 border-b border-white/10 flex items-center justify-between">
        <div className="flex items-center gap-2">
          <div className="w-7 h-7 bg-yellow-400 rounded flex items-center justify-center shrink-0">
            <svg className="w-4 h-4 text-blue-900" fill="currentColor" viewBox="0 0 24 24">
              <path d="M13 2L3 14h9l-1 8 10-12h-9l1-8z" />
            </svg>
          </div>
          <span className="text-white font-semibold text-base">CareTend Fax</span>
        </div>
        {onClose && (
          <button onClick={onClose} className="text-blue-200 hover:text-white lg:hidden">
            <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M6 18L18 6M6 6l12 12" />
            </svg>
          </button>
        )}
      </div>

      {/* Nav */}
      <nav className="flex-1 overflow-y-auto py-3 space-y-1">
        {sections.map((section) => {
          if (section.adminOnly && !isAdmin) return null
          return (
            <div key={section.title}>
              <button
                onClick={() => toggle(section.title)}
                className="w-full flex items-center justify-between px-4 py-2 text-xs font-semibold uppercase tracking-widest text-blue-200 hover:text-white transition-colors"
              >
                {section.title}
                <svg className={`w-3.5 h-3.5 transition-transform ${open[section.title] ? "rotate-180" : ""}`} fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2.5} d="M19 9l-7 7-7-7" />
                </svg>
              </button>

              {open[section.title] && (
                <div className="mt-1 mb-2">
                  {section.items.map(({ href, label }) => {
                    const active = pathname === href
                    const badge = label === "Received Faxes" && unreadCount > 0 ? unreadCount : null
                    return (
                      <Link
                        key={href}
                        href={href}
                        onClick={onClose}
                        className={`flex items-center gap-2.5 px-4 py-1.5 text-sm transition-colors ${
                          active ? "bg-white/15 text-white font-medium" : "text-blue-100 hover:bg-white/10 hover:text-white"
                        }`}
                      >
                        <span className="opacity-70 shrink-0">{icons[label]}</span>
                        <span className="flex-1">{label}</span>
                        {badge != null && (
                          <span className="text-xs bg-blue-500 text-white px-1.5 py-0.5 rounded-full leading-none min-w-5 text-center">
                            {badge > 99 ? "99+" : badge}
                          </span>
                        )}
                      </Link>
                    )
                  })}
                </div>
              )}
            </div>
          )
        })}
      </nav>

      {/* Sign out */}
      <div className="border-t border-white/10 px-4 py-3">
        <button
          onClick={() => signOut({ callbackUrl: "/login" })}
          className="flex items-center gap-2 text-sm text-blue-200 hover:text-white transition-colors w-full"
        >
          <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={1.8} d="M15.75 9V5.25A2.25 2.25 0 0013.5 3h-6a2.25 2.25 0 00-2.25 2.25v13.5A2.25 2.25 0 007.5 21h6a2.25 2.25 0 002.25-2.25V15M12 9l-3 3m0 0l3 3m-3-3h12.75" />
          </svg>
          Log Out
        </button>
      </div>
    </>
  )
}

interface Props {
  role: string
  unreadCount: number
}

export default function Sidebar({ role, unreadCount }: Props) {
  const [mobileOpen, setMobileOpen] = useState(false)

  return (
    <>
      {/* Mobile hamburger button */}
      <button
        onClick={() => setMobileOpen(true)}
        className="lg:hidden fixed top-4 left-4 z-30 p-2 rounded-lg text-white shadow-lg"
        style={{ backgroundColor: "#1e3a6e" }}
        aria-label="Open menu"
      >
        <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
          <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M4 6h16M4 12h16M4 18h16" />
        </svg>
      </button>

      {/* Mobile overlay */}
      {mobileOpen && (
        <div className="lg:hidden fixed inset-0 z-40 flex">
          <div className="fixed inset-0 bg-black/50" onClick={() => setMobileOpen(false)} />
          <aside className="relative z-50 w-56 flex flex-col h-full" style={{ backgroundColor: "#1e3a6e" }}>
            <SidebarContent role={role} unreadCount={unreadCount} onClose={() => setMobileOpen(false)} />
          </aside>
        </div>
      )}

      {/* Desktop sidebar */}
      <aside className="hidden lg:flex w-52 shrink-0 flex-col h-full" style={{ backgroundColor: "#1e3a6e" }}>
        <SidebarContent role={role} unreadCount={unreadCount} />
      </aside>
    </>
  )
}
